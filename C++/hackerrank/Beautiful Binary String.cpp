// Complete the beautifulBinaryString function below.
int beautifulBinaryString(string b) {
    int cnt = 0;
    for(int i = 0; i < b.size(); i++){
        if(b[i] == '0' && b[i+1] == '1' && b[i+2] == '0'){
            b[i+2] = '1';
            cnt++;
        }
    }
    return cnt;
}

