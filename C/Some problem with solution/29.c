//29.Write a C program to input week number and print week day.

#include<stdio.h>
int main()
{
    int n;
    printf("Enter the week number.\n");
    scanf("%d",&n);
    if(n == 1){
        printf("Saturday.\n");
    }
    else if(n == 2){
        printf("Sunday.\n");
    }
    else if(n == 3){
        printf("Monday.\n");
    }
    else if(n == 4){
        printf("Tuesday.\n");
    }
    else if(n == 5){
        printf("Wednesday.\n");
    }else if(n == 6){
        printf("Thursday.\n");
    }
    else if(n == 7){
        printf("Friday.\n");
    }
    return 0;
}
